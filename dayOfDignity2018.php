<p class="smaller">
585 homeless were served with about 385 children impacted.
<br> <br>
Supplies provided to the homeless include: <br>
Customized backpacks with heavy duty ponchos for the homeless from CityPak (Chicago, Illinois), 0 degree temperature sleeping bags from Als Sports (Salt Lake City, Utah), street socks from Bombas donated to Portland Refugee Support Group, thermal socks, hygiene kits, female sanitary supplies and school kits. 
<br><br>
Services provided to the homeless include: <br> 
Chiropractic treatment, medical application registration assistance and counseling, blood pressure; glucose testing, dental hygienist counseling, housing/shelter resources and job resources.
<br><br>
Iraqi and Syrian refugees from IRCO-Mideast House also volunteered their services by providing sandwiches from Dar Salam Restaurant. 

