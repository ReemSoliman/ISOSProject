    <h2 class="pageTitle">Day Of Dignity 2016</h2>
    
    <p class="smaller"> This year’s day of Dignity fell on the 15th anniversary of
the 9/11 tragedy. The location was held downtown Portland in the Pearl District park. The number of agencies that
participated with us increased by 33% from last year. This year we had: <br>
 <img src="/images/dayofdignity2016flyer.jpg" alt="Day of Dignity 2016 flyer" style="float:right;width:20%;height:9%; padding-right:1em;">
<ul>
<li>Oregon Islamic Chaplain Organization (OICO) </li>
<li>Blanchet House    </li>
<li> Latter Day Saints (LDS) </li>
<li> Jewish Family and Child Services   </li>
<li> Japanese American Citizen League (JACL)</li>
<li> Multnomah Health Dept  </li>
<li> Immaculate Heart of Mary Catholic Church   </li>
<li> New Season Market    </li>
<li> Costco         </li>
<li>Oregon Food Bank </li>
<li> Street Roots   </li>
<li> Sesame Donuts </li>
</ul>
<br><br>
Our partnership was our way in showcasing true American spirit
in working together for a better America, united for the greater cause of humanity. We had 546 homeless served with a
bar-b-que picnic. Supplies distributed to the homeless were thermal socks; ponchos; hoodies; knitted caps; mittens;
scarves; 1st aid kits; school kits; and hygiene kits. In addition, resource information; health counselors and barbers for
haircuts were made available.</p>
