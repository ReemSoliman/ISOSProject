 <p class="smaller">
 Our local youth from 12-18 years of age provided their time and services with packaging and manning booths and
distributing the supplies. For our youth, this is an excellent way to gain awareness and respect for the human condition. <br>
  We want to groom our youth to take more leadership roles with community well-being
activities and to pass the baton over to the next generation.

