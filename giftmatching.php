<h2 class="pageTitle">Employee Matching Gift</h2>

<pre>
Employee matching gifts (also known as matching funds) are grants an employer makes to match its employees’ charitable contributions. 
Corporates like Intel, Mentor Graphics, Nike and many others corporates operate grantmakers, which match employee donations, employee 
matching gifts often are dollar-for-dollar, but some companies will give double or even triple the original donation.

If your company matches donations made to the ISOS, here are two ways to initiate the matching gift contribution:

1. If your company uses an online matching gift platform, submit a matching gift verification request through your company’s online portal 
using the address of the ISOS.

OR

2. Obtain the appropriate matching gift verification form from your personnel/human resources office. Confirm the organizations official 
name and complete the form, most probably you will need to attach a receipt from the ISOS of your donation.

